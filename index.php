                  <p>2.System Analytics/
                    อาชีพนักวิเคราะห์ระบบ (SA) ก็คือ ผู้วิเคราะห์ปัญหาและนำมาเปลี่ยนให้เป็นรูปที่เหมาะสมสำหรับการประเมินค่าด้วยเครื่องคอมพิวเตอร์ รวมถึงการวางแผนงาน การสั่ง การทบทวนโครงการ ประมวลผลโดยคอมพิวเตอร์ และการจัดทำรายงานเกี่ยวกับโครงการที่ทำไปแล้ว พิจารณานำระบบคอมพิวเตอร์มาใช้เพื่อช่วยในการดำเนินงาน นักวิเคราะห์ระบบ (SA) ต้องพิจารณาทั้งการนำเครื่องคอมพิวเตอร์และอุปกรณ์ (Hardware) โปรแกรมควบคุมระบบ/โปรแกรมอื่นๆ (Software) ระบบเครือข่าย (Network) และบุคลากร (People) มาใช้งานให้ได้อย่างเหมาะสม
                    ตำแหน่งงานนักวิเคราะห์ระบบ (SA) คืออะไร และมีหน้าที่อะไรบ้าง?
                    ผู้ที่ทำหน้าที่ศึกษาปัญหาและความต้องการขององค์กรในการกำหนดบุคคล ข้อมูล การประมวลผล การสื่อสาร และเทคโนโลยีสารสนเทศ (Information Technology) ว่าจะจัดการหรือปรับปรุงอย่างไรเพื่อที่จะสามารถพัฒนาระบบธุรกิจไปสู่ความสำเร็จได้ โดยจะทำการศึกษาปัญหา รวบรวมความต้องการของระบบ วิเคราะห์ระบบงานธุรกิจ ตรวจสอบว่าจะนำระบบสารสนเทศมาใช้หรือไม่ หรือควรจะปรับปรุงระบบเดิม เขียนข้อกำหนดและรายละเอียดของระบบใหม่ เลือกฮาร์ดแวร์ (Hardware) และซอฟต์แวร์ (Software) ใดที่เหมาะสมกับองค์กร มีการวิเคราะห์ต้นทุนว่าคุ้มกับการที่จะลงทุนเปลี่ยนระบบใหม่หรือไม่ หรือมีทางใดที่จะช่วยให้ระบบสามารถสนับสนุนความต้องการขององค์กรได้เป็นอย่างดี
                    
                  <p><b>ทักษะและความรู้ที่ต้องมี</b></p>
                  <a><li>รวบรวมข้อกำหนดความต้องการใช้งาน (Requirement)</li></a>
                  <a><li>ศึกษาระบบเดิมเพื่อหาแนวทางการแก้ไขปัญหา และให้คำแนะนำเกี่ยวกับระบบ</li></a>
                  <a><li>วิเคราะห์และออกแบบระบบ ให้สอดคล้องกับความต้องการใช้งาน</li></a>
                  <a><li>วางแผนการดำเนินงานการพัฒนาระบบ</li></a>
