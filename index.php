                    <p>สิ่งหนึ่งที่คุณควรทราบ คุณอาจไม่เห็นรายชื่อจำนวนมากที่แสดงตำแหน่งสายงานที่แจ้งว่า บริษัท กำลังมองหา “นักพัฒนา Back End ” แต่คุณจะพบรายการที่แจ้งว่าทางบริษัทกำลังมองหา นักพัฒนา Ruby หรือ นักพัฒนา PHP ฯลฯ เป็นต้น เนื่องจากภาษาการเขียนโปรแกรมนั้น มีแค่นักพัฒนาที่รู้ว่ามันคือกุญแจสำคัญที่เหมาะสมกับงานชนิดใดบ้าง
                      นักพัฒนา Back End ส่วนใหญ่จะทำงานร่วมกับนักพัฒนา Front End เพื่อทำให้ Code ทำงานในการออกแบบเว็บไซต์ หรือการออกแบบแอปพลิเคชัน รวมถึงการปรับแต่งการออกแบบนั้นเมื่อจำเป็น ซึ่งในที่สุดแล้วก็มาถึงหัวข้อสุดท้ายของ Full Stack
                      </p>
                    <a><b>ทักษะและความรู้ที่ต้องมี</b></a>
                    <a><li>มีความรู้ในภาษาโปรแกรมอย่างน้อย 1-2 ภาษา อาจจะเน้นไปที่ภาษาที่เป็นความต้องการของตลาด เช่น Node.JS, Java, Python, PHP, C# หรือ Go เป็นต้น</li></a>
                    <a><li>ควรมีความรู้พื้นฐานในด้าน Package Management Tool เพื่อใช้สำหรับจัดการ library/dependency ต่าง ๆ ที่นำมาใช้ในระบบ ซึ่งแต่ละภาษาก็ใช้เครื่องมือที่แตกต่างกัน</li></a>
                    <a><li>ควรมีความสามารถในการเขียนการทดสอบอัตโนมัติ เช่น Unit test, Integration test, API test, Component test และ End-to-End test</li></a>
                    <a><li>ควรมีความรู้ในด้าน Database เพราะเป็นสิ่งที่สำคัญมากต่อระบบหลังบ้าน</li></a>
