                    <p>5.Software Tester
                      คือคนที่คอยตรวจสอบคุณภาพของซอล์ฟแวร์ที่ใช้งานภายในองค์กร รวมถึงทดสอบโปรแกรมต่างๆ ที่สร้างขึ้นมาโดยโปรแกรมเมอร์ก่อนส่งให้กับลูกค้าด้วย เมื่อไรที่พบความผิดพลาดจะได้แจ้งแก้ไขให้เรียบร้อยก่อนการดำเนินงานขั้นตอนต่อไป อย่างไรก็ตามในส่วนของเนื้องานที่ต้องรับผิดชอบจริงๆ ของ software tester มันจะแปรผันไปตามความต้องการของบริษัทผู้ว่าจ้าง เหมือนกับที่เกริ่นไปแล้วข้างต้นว่า บางที่ก็จะมอบงานหลายด้านให้กับคนๆ เดียว บางที่ก็จะตีกรอบงานที่เฉพาะเจาะจงมาให้เพื่อหวังผลในประสิทธิภาพของเนื้องานมากที่สุด                      
                    <p><b>หน้าที่ทั่วไปของ Tester </b></p>
                    <p>1.	คิดกรณีที่ทำให้การทำงานของระบบหรือ application ทุกกรณีที่เป็นไปได้ว่าจะเกิดขึ้น – design test cases</p>
                    <p>2.	เขียนขั้นตอนการ test โดยคิดว่าคนอื่นสามารถอ่านแล้วเข้าใจ – create / produce test cases</p>
                    <p>3.	ติดตั้งระบบเพื่อใช้ในการ test – setup environment</p>
                    <p>4.	ทำ test script ในกรณีที่ตกลงกันว่าจะทำ – produce test script / test automation</p>
                    <p>5.	เริ่มการ test ระบบ – execute testing</p>
                    <p>6.	เขียนสรุปผลการ test เพื่อรายงานให้ผู้ที่เกี่ยวข้องรู้ – produce test report</p>

