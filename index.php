                      ในส่วนของหน้าเว็บไซต์ หรือแอปพลิเคชันบนมือถือ เป็นส่วนที่ผู้ใช้สามารถมองเห็น และโต้ตอบได้โดยตรง ถูกสร้างขึ้นด้วยภาษาทั่วไปที่ต้องรู้ ดังตัวอย่างต่อไปนี้ 
